%% Plot Arcturus Accelerometer Data

xdata = {openrocket_VarName1, openrocket_VarName1, openrocket_VarName1};
ydata =...
{...
    openrocket_Machnumber,...
    openrocket_Totalaccelerationms,...
    openrocket_Altitude...
};

ylabel = ...
{...
    'Mach Number',...
    'Acceleration',...
    'Altitude'...
};

linespec = {'b','b','b'};

multiplot(xdata, ydata, 'YLabel', ylabel, ...
 'LineSpec', linespec, 'Title', 'Arcturus OpenRocket Simulation Data', 'XLabel', 'time');

if exist('saveplots')
    if saveplots % conditionally save the plot
        saveas(gcf, '../documentation/images/plots/arcturus_openrocket_plot.png');
        export_fig arcturus_openrocket_plot.png -m2
    end
end
